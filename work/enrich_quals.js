let filled = 0, kept = 0, unmatched = [];
for (const u of j.ippan.universities) {
  for (const d of (u.departments || [])) {
    if (Array.isArray(d.qualifications) && d.qualifications.length > 0) { kept++; continue; }
    const rule = RULES.find(([rx]) => rx.test(d.name || ''));
    if (rule) {
      d.qualifications = rule[1].slice();
      d.quals_estimated = true; // 系統からの目安であることを示すフラグ
      filled++;
    } else {
      unmatched.push(`${u.name}: ${d.name}`);
    }
  }
}

